Nikon D5500 ซึ่งเป็นกล้อง DX ความละเอียด 24.2 ล้านพิกเซลที่ออกแบบมาโดยเฉพาะเพื่อนักถ่ายภาพที่เต็มเปี่ยมไปด้วยความปราถนาในการสรรหาวิธีใหม่ๆเพื่อให้ได้ภาพถ่ายที่น่าตื่นตากว่าที่เคย 
Nikon D5500 เป็นกล้อง DSLR ที่เล็กที่สุด เบาที่สุดและบางที่สุดในโลก ที่มาพร้อมความสามารถในการเชื่อมต่อแบบไร้สาย ช่วยให้การทำงานลื่นไหลใช้ง่ายดั่งใจ 
เพื่อผลงานภาพที่ยิ่งใหญ่เผยแรงบันดาลใจได้เต็มเปี่ยม ทั้งในรูปแบบของภาพนิ่งหรือภาพเคลื่อนไหว นอกจากนี้ ยังเพิ่มขีดความสามารถด้วยหน้าจอ LCD 
แบบปรับหมุนได้หลากหลายองศาตามต้องการ ควบคุมการทำงานด้วยระบบหน้าจอสัมผัส เซ็นเซอร์ตรวจจับดวงตาอัจฉริยะ และระบบปรับแต่งภาพที่ได้รับการปรับปรุงเต็มประสิทธิภาพ

