กล้องดิจิตอล Nikon D7100 ให้ภาพถ่ายมีคุณภาพที่น่าตื่นตะลึงเช่นเดียวกับกล้อง D-SLR ระดับแนวหน้าของเรา เปี่ยมประสิทธิภาพด้วยเซนเซอร์ CMOS ความละเอียด 24.1 ล้านพิกเซลในรูปแบบ DX 
พร้อมระบบ EXPEED 3 ที่ยอดเยี่ยม บันทึกภาพนิ่งหรือวิดีโอได้ในทุกสภาวะแสง ด้วยระบบลดสัญญานรบกวนและมีระดับความไวแสงขั้นสูง และเพื่อความคมชัดสูงสุดเมื่อถ่ายภาพที่มีรายละเอียดมาก D7100 
จึงไม่ใช้โลว์พาสฟิลเตอร์ซึ่งเหมาะอย่างยิ่งสำหรับการถ่ายภาพทิวทัศน์ 

• เซนเซอร์ CMOS APS-C ความละเอียด 24.1 ล้านพิกเซล(ไม่มี low-pass filter)
• ชิพประมวลผล EXPEED 3
• ISO100-6400 และขยายได้ถึง 25600
• ถ่ายภาพต่อเนื่องความเร็วสูงสุด 7 ภาพต่อวินาที(ในโหมด crop 1.3x)
• จุดโฟกัส 51 จุด cross-type 15 จุด ที่ใช้เทคโนโลยีเทียบเท่า D4
• เซนเซอร์วัดแสง 2,016 พิกเซล
• ช่องมองภาพครอบคลุมการมองเห็นได้ 100%
• บอดี้แมกนีเซียมอัลลอยด์(เฉพาะด้านบนและด้านหลัง)
• ซีลกันฝุ่นและละอองน้ำ
• เซนเซอร์วัดระดับน้ำสามารถแสดงผลตามการพลิกของกล้อง
• จอแสดงผลขนาด 3.2" ความละเอียด 1,229,000 พิกเซล (แบบไม่มีช่องว่างระหว่างหน้าจอและกระจก)
• ถ่าย VDO Full HD 1080p @60i/50i/30p/25p/24p และ 720p @60p/50p
• ช่องใส่ SD Card 2 Slot
• มีโหมด crop 1.3x
• ใช้กับ Grip MB-D15

