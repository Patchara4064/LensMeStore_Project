กล้อง Nikon D5600 กล้อง DSLR ระดับ Mid-Range เมื่อเทียบกับตัวเก่าอย่าง Nikon D5500 ทางนิคคอนได้เพิ่ม SnapBrige และ ฟังก์ชั่น Time Lapes ในตัวกล้องเข้ามา 
ส่วนเซ็นเซอร์ยังใช้รุ่นเดิมNikon D5600 มีหน้าจอสัมผัสแบบหมุนได้ 360 องศาขนาด 3.2 นิ้ว คล้ายรุ่นเก่า แต่รุ่นใหม่ เมื่อใช้ View Finder สามารถใช้จอสัมผัสเลื่อนจุดโฟกัสผ่านหน้าจอสัมผัสได้ 
เซ็นเซอร์ใช้ DX-Format CMOS ความละเอียด 24.2 MP ระบบประมวลผล EXPEED 4 สามารถเชื่อมต่อ SnapBrige ฟีเจอร์ใหม่จาก Nikon ที่เราสามารถโอนรูปภาพจากกล้องไปมือถือโดยไม่ต้องเปิดกล้อง
 นอกจากนี้ยังเพิ่มฟังก์ชั่นใหม่ อาทิ การถ่ายวิดิโอ Time Lapes เข้ามาในตัวกล้องกล้อง Nikon D5600 มาพร้อมกับ 4 ทางเลือก Body เปล่า, พร้อมเลนส์ AF-P 18-55 mm f/3.5-5.6 G VR,
 AF-S 18-140 mm f/3.5-5.6G ED VR และ AF-P 18-55 mm f/3.5-5.6 G VR + AF-P 70-300 f/4.5-6.3 ED VR เริ่มขายในต่างประเทศ พฤศจิกายน 2559

• 24.3MP Full-frame เซ็นเซอร์ CMOS ( 10.5MP DX-format crop mode)
• ISO 100-6400 ( ขยายได้ถึง ISO 50-25,600 เท่า)
• ถ่ายภาพต่อเนื่องสูงสุด 6fps ใหม่ โหมดเงียบ ถ่ายอย่างต่อเนื่อง shoots at 3 fps
• ออโต้โฟกัส 39 จุด 9 cross-type AF points
• white balance อัตโนมัติ ระบบปรับสมดุล
• Wireless flash
• หน้าจอ แอลซีดี 3.2in 921K จุด
• ช่องเสียบการ์ด หน่วยความจำ แบบ Dual
• วิดีโอ full HD 1080p30
• การบันทึกวิดีโอ การบีบอัด ผ่าน HDMI

