Nikon D610 ด้วยเซนเซอร์ CMOS 24.3 ล้านพิกเซล รูปแบบไฟล์ FX และโปรเซสเซอร์ EXPEED 3 ประสิทธิภาพสูง D610 จึงส่งมอบภาพถ่ายที่เปี่ยมด้วยสีสันและรายละเอียดที่น่าจดจำ 
ช่วงความไวแสงระดับกว้างจาก ISO100 ถึง 6400 (ขยายได้จาก ISO50 ถึง ISO25,600) รับรองคุณภาพที่เต็มเปี่ยมภายใต้สภาวะแสงใดๆ ด้วยรูปลักษณ์ที่เล็กกระทัดรัดและนํ้าหนักของตัวกล้องเพียง 760 กรัม D610
จึงเหมาะอย่างยิ่งสำหรับการเดินทางระยะไกลและการถ่ายภาพนาน ๆ โครงสร้างทำด้วยโลหะผสมแมกนีเซียมและมีความคงทนต่อสภาวะอากาศ เป็นอย่างดี กล้องรุ่นนี้จึงสามารถรับมือกับการใช้งานกลางแจ้งได้อย่างทนทาน
เพื่อความสามารถในการถ่ายภาพที่มากยิ่งขึ้น D610 ใช้งานได้อย่างเต็มที่กับเลนส์ NIKKOR ทั้งในรูปแบบไฟล์ FX และ DX พร้อมเลือกโหมดการตัดขอบภาพ DX โดยอัตโนมัติเพื่อหลีกเลี่ยงการจัดเรียงภาพเล็ก ๆ

• 24.3MP Full-frame เซ็นเซอร์ CMOS ( 10.5MP DX-format crop mode)
• ISO 100-6400 ( ขยายได้ถึง ISO 50-25,600 เท่า)
• ถ่ายภาพต่อเนื่องสูงสุด 6fps ใหม่ โหมดเงียบ ถ่ายอย่างต่อเนื่อง shoots at 3 fps
• ออโต้โฟกัส 39 จุด 9 cross-type AF points
• white balance อัตโนมัติ ระบบปรับสมดุล
• Wireless flash
• หน้าจอ แอลซีดี 3.2in 921K จุด
• ช่องเสียบการ์ด หน่วยความจำ แบบ Dual
• วิดีโอ full HD 1080p30
• การบันทึกวิดีโอ การบีบอัด ผ่าน HDMI

