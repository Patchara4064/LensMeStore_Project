ตัวกล้องของ X-T2 ใช้เซนเซอร์ 24.3 ล้านพิกเซล X-Trans CMOS III APS-C ซึ่งเป็นตัวเดียวกับ X-Pro2 พร้อมกับระบบ hybrid AF ที่อัพเกรดใหม่ รวมถึงมี joystick สำหรับเลือกจุดโฟกัสอัตโนมัติเหมือนกับ X-Pro2 
และมีความเร็วชัตเตอร์สูงสุดในทางกลไกที่ 1/8000 และในทางอิเล็กทรอนิกส์ที่ 1/32000 และปรับปรุงอัลกอริทึมของ AF-C ซึ่งจะช่วยให้การตอบสนองของตัวกล้องเร็วขึ้น
ส่วนการแสดงผลนั้น X-T2 ใช้ช่องมองภาพอิเล็กทรอนิกส์แบบ OLED ที่ 2.36 ล้านจุด มี lag time 0.005 วินาที อัตราการขยาย 0.77x เท่า ส่วนจอหลักเป็น LCD ขนาด 3 นิ้ว 1.04 ล้านจุด พร้อมบานพับ
ตัวกล้องทำจากแมกนีเซียมอัลลอยด์ พร้อมการซีลกันน้ำกันฝุ่น 63 จุด สามารถใช้งานได้ในสถานที่ที่อุณหภูมิต่ำสุดถึง 14 องศาฟาเรนไฮต์ (-10 องศาเซลเซียส)
นอกจากนี้ยังมีแฟลชตัวใหม่ EF-X500 ไว้สำหรับเสียบกับ hot shoe ของตัวกล้อง รองรับโหมด FP (high-speed flash sync) ซึ่งสามารถใช้งานได้ทุกค่าความเร็วชัตเตอร์ รวมถึงสามารถตั้งค่า multi-flash 
สำหรับใช้งานกับแฟลชตัวอื่นได้ด้วย และสามารถใช้โหมด TTL ได้ทั้งโหมด single-flash และ multi-flash



