Fujifilm ได้เปิดตัวกล้อง Fujifilm X-T20 กล้อง Mirrorless ในระดับ Semi Pro ชูจุดเด่นด้วยเซ็นเซอร์ X-Trans CMOS III ความละเอียด 24.3 MP และ โฟกัส 325 จุด
กล้อง Fujifilm X-T20 หรือ Fuji X-T20 กล้องในระดับ Semi Pro หรือ กึ่งมืออาชีพ สามารถใช้งานได้ไม่ยาก แต่ก็ฟังก์ชันครบสำหรับมืออาชีพ ระบบประมวลผล X-Processer Pro เซ็นเซอร์ ขนาดใหญ่ APS-C X-Trans CMOS III 
ความละเอียด 24.3 MP จุดโฟกัสมากถึง 325 จุด ทำให้โฟกัสได้รวดเร็ว ทุกสถานการณ์ ที่สำคัญมี Custom Sitting ให้ใช้งานอีกด้วยหน้าจอสัมผัสขนาดใหญ่ถึง 3 นิ้ว EVF ขนาดใหญ่ ความละเอียด 2.36 ล้านจุด วิดีโอสามารถบันทึกได้ความละเอียดสูงถึง 3840×2160 
หรือ 4K ที่เฟรมเรต 30 fps นานต่อเนื่อง 10 นาที และ ถ่ายต่อเนื่อง 8 ภาพต่อวินาที (และ 14 ภาพต่อวินาที สำหรับ Electronic Shutter)แบตเตอรี่รุ่นใหม่ NP-126s สามารถชาร์จผ่าน PowerBank ได้ผ่านพอร์ท Micro USB 
ตัวกล้องขนาดเล็กเบาเพียง 383 กรัม มีให้เลือก 2 สี นั้นก็คือสี ดำ และ เงิน ข้อเสียคือไม่สามารถ กันละอองน้ำ ละอองฝุ่นได้


• Sensor X-Trans CMOS III ความละเอียด 24.3 megapixel 
• Processor X-Processor Pro
• โฟกัสได้เร็ว 0.06 วินาที
• บันทึก Video ความละเอียด 4K 30fps, 1080@60p
• หน้าจอ Touchscreen  3 นิ้ว สามารถปรับองศาการมองได้
• Electronic viewfinder  ความละเอียด 2.36 million dots
• ถ่ายภาพต่อเนื่อง 8 fps หรือ 16fps ผ่าน Electronic Shutter
• Electronic shutter 1/32000
• Film simulation “ACROS”
• BuiltIn wifi




