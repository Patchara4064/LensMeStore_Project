Fujifilm ได้เปิดตัวกล้องรุ่นใหม่ X100F โดยเป็นกล้องคอมแพครุ่นต่อจาก X100 โดยปรับปรุงตัวกล้องไปใช้เซนเซอร์ APS-C แบบ X-Trans III ที่ 24.3 ล้านพิกเซล, หน่วยประมวลผลภาพ X-Processor Pro แบบเดียวกับ X-T2, เพิ่มจุดโฟกัสจาก 49 เป็น 91
Fujifilm เคลมว่าตัว X100F จะเร็วขึ้นในทุกด้าน ไม่ว่าจะเป็นการโฟกัสที่ใช้เวลาเพียง 0.08 วินาที, ใช้เวลาเปิดเครื่อง 0.5 วินาที และลั่นชัตเตอร์ 0.2 วินาที
ตัวบอดี้กล้อง จะมี AF joystick สำหรับการเลื่อนหาจุดโฟกัส, การควบคุมอื่น ๆ จะถูกขยับไปไว้ด้านขวาของหน้าจอหลัก, ด้านบนของวงแหวนปรับความเร็วชัตเตอร์จะเป็นวงแหวนควบคุม ISO, 
เพิ่มวงแหวนควบคุมด้านหน้า, ช่องมองภาพแบบผสมที่สลับไปมาได้ระหว่างโหมดอิเล็กทรอนิกส์และโหมดปกติ สามารถตั้งค่าการขยายภาพได้เมื่อใช้งานในโหมดอิเล็กทรอนิกส์

